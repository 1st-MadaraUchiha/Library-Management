    public boolean issueBook() {
        int bookId = Integer.parseInt(bookid_text.getText());
        int count=0;
        boolean doNotIssue = true;
        boolean isIssued = false;
        String statuss = "pending";
        int studentId = Integer.parseInt(studentId_text.getText());


        try {
            ResultSet rs = con.s.executeQuery("select quantity from book_details where book_id ='" + bookId + "' ");
            while (rs.next()) {
                count = rs.getInt("quantity");
            }
            ResultSet sr = con.s.executeQuery("select book_id from issue_book_details where student_id ='"+studentId+"' and book_id='"+bookId+"' and status='"+statuss+"'");
            if(sr.next()) doNotIssue = true;
            else if(!sr.next()) doNotIssue = false;
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (count> 0 && !doNotIssue) {


            String bookName = bookname_lb.getText();
            String studentName = studentName_lb.getText();

            Date uIssueDate = issueDate.getDatoFecha();
            Date uDueDate = dueDate.getDatoFecha();

            Long l1 = uIssueDate.getTime();
            Long l2 = uDueDate.getTime();

            java.sql.Date sIssueDate = new java.sql.Date(l1);
            java.sql.Date sDueDate = new java.sql.Date(l2);
            String pending = "pending";
            String sql = "insert into issue_book_details (book_id,book_name,student_id,student_name,issue_date,due_date,status) values('" + bookId + "','" + bookName + "','" + studentId + "','" + studentName + "','" + sIssueDate + "','" + sDueDate + "','" + pending + "')";

            try {
                con.s.execute(sql);
                isIssued = true;
                bookCount();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        else if (count<1 ){
            isIssued = false;
            JOptionPane.showMessageDialog(null,"There is no book left that can be issued");
        }
        else if(doNotIssue) JOptionPane.showMessageDialog(null,"This book was already issued to student");
            return isIssued;
        }

