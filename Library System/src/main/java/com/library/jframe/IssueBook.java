    private void setBookDetailsToLabel(String bookid){
        try{
            
            ResultSet rs = con.s.executeQuery("select * from book_details where book_id='"+bookid+"'");
            while(rs.next()){
                bookid_lb.setText(rs.getString("book_id"));
                if(bookid_lb == null ){
                    JOptionPane.showMessageDialog(null,"No book with given bookid");
                    this.dispose();
                    IssueBook.main(null);
                }
                bookname_lb.setText(rs.getString("book_name"));
                authorName_lb.setText(rs.getString("author"));
                quantity_lb.setText(rs.getString("quantity"));
                
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
