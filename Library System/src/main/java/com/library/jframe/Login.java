    @Override
    public void actionPerformed(ActionEvent e){
     if(e.getSource()==login_button){
         username= username_text.getText();
         char[] pass = password_text.getPassword();
         password = new String(pass);
         if(check()){
             JOptionPane.showMessageDialog(null,"Logged in successfully");
             } 
         
     }
     else if(e.getSource()==signuo_button1){
         new SignUpForm().setVisible(true);
         this.setVisible(false);
     }
    }
    public boolean check(){
        try {
            con = new MySQLConnection();
            String ss = "select * from users where name='" + username + "' and password = '" + password + "'";
            ResultSet set = con.s.executeQuery(ss);
            if(set.next()){
            return true;
            }
            else if(!set.next()){
                JOptionPane.showMessageDialog(null,"Incorrect Credentials");
            }
        }
        catch(Exception ae){
            ae.printStackTrace();
        }
        return false;
    }
