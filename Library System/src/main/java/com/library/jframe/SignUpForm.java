    @Override
    public void actionPerformed(ActionEvent e){
     if(e.getSource()==signuo_button1){
         username= username_text.getText();
         char[] pass = password_text.getPassword();
         String password = new String(pass);
         String email = email_text.getText();
         String phone = contact_text.getText();
//         System.out.println(username+"o"+email+"p"+phone+"q"+password);
//         System.out.println(email);
//         System.out.println(phone);
//         System.out.println(password);

         if(check()) {
             JOptionPane.showMessageDialog(null,"User name already exists");
         }
         else if(!check()) {

             if ((username != null && username.length()>4) && (password !=null && password.length()>3) && (email !=null && email.length()>5) && (phone !=null && phone.length()>9)) {

                 try {
                     con = new MySQLConnection();
                     String str = "insert into users(name,password,email,contact) values('" + username + "','" + password + "','" + email + "','" + phone + "')";
                     con.s.executeUpdate(str);
                     JOptionPane.showMessageDialog(null, "Data Added to database successfully");
                 } catch (Exception ea) {
                     ea.printStackTrace();
                     JOptionPane.showMessageDialog(null, "Error");
                 }
             } else {
                 JOptionPane.showMessageDialog(null, "Please enter all the information. Username min 4 char,password min 5 charcaters,and phone min 10 characters ");
             }
         }
     }
     if(e.getSource()==login_button){
         this.setVisible(false);
         new Login().setVisible(true);

     }
    }
    public boolean check(){
        try {
            con = new MySQLConnection();
            String ss = "select name from users where name ='"+username+"'";
            ResultSet set = con.s.executeQuery(ss);
            if(set.next()){
            return true;
            }
        }
        catch(Exception ae){
            ae.printStackTrace();
        }
        return false;
    }
