    private void clearRows(){
        model = (DefaultTableModel)issuedBooksDetails_tbl.getModel();
        model.setRowCount(0);
    }
    private void search(){
        String hql = null;
        String studentId = studentID_text.getText();
        String comboData = comboBox.getSelectedItem().toString();
        switch (comboData) {
            case "Pending" -> hql ="select * from issue_book_details where student_id ='"+studentId+"' and status ='"+"pending"+"'";
            case "Returned" -> hql ="select * from issue_book_details where student_id ='"+studentId+"' and status = '"+"returned"+"'";
            case "All" -> hql = "select * from issue_book_details where student_id ='"+studentId+"'";
            default -> {
            }
        }
        try{ con = new MySQLConnection();
            ResultSet rss = con.s.executeQuery(hql);
            if(!rss.next()){
                JOptionPane.showMessageDialog(null,"No Student with that book Id or book status");
            }
            else if(rss.next()){
                ResultSet rs = con.s.executeQuery(hql);
                while(rs.next()){
                int id = rs.getInt("id");
                String bookName = rs.getString("book_name");
                String studentName = rs.getString("student_name");
                String issuedDate = rs.getString("issue_date");
                String dueDate = rs.getString("due_date");
                String status = rs.getString("status");
                
                Object[] obj = {id,bookName,studentName,issuedDate,dueDate,status};
                model =(DefaultTableModel) issuedBooksDetails_tbl.getModel();
                model.addRow(obj);
            }
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
