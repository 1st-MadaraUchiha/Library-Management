    private void clearRows(){
        model = (DefaultTableModel)issuedBooksDetails_tbl.getModel();
        model.setRowCount(0);
    }
    private void search(){
        Long time = System.currentTimeMillis();
        Date today = new Date(time);
                try{ con = new MySQLConnection();
                String hql ="select * from issue_book_details where due_date < '"+today+"'";
                ResultSet rs = con.s.executeQuery(hql);
                while(rs.next()){
                int id = rs.getInt("id");
                String bookName = rs.getString("book_name");
                String studentName = rs.getString("student_name");
                String issuedDate = rs.getString("issue_date");
                String dueDate = rs.getString("due_date");
                String status = rs.getString("status");
                
                Object[] obj = {id,bookName,studentName,issuedDate,dueDate,status};
                model =(DefaultTableModel) issuedBooksDetails_tbl.getModel();
                model.addRow(obj);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
